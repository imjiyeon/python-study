# 리스트 인덱싱

# 리스트의 각 요소는 인덱스 번호를 가지며,
# 인덱스 번호로 각 요소에 접근할 수 있다
# index: 0~2
list = [1, 2, 3]
print(list)

# 리스트의 첫번째 요소
print(list[0])

# 리스트의 마지막 요소
print(list[2])
print(list[-1])


# 2번째 리스트
item = [1, "apple", 3, "banana"]
print(item)

# 첫번째 요소
print(item[0])
# 두번째 요소
print(item[1])
# 마지막 요소
print(item[3])
print(item[-1])
