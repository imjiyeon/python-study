# 다음과 같이 학생 점수 딕셔너리를 만드세요
scores = {"수학": 95, "영어": 88, "국어": 100}

# 영어와 국어의 평균 점수를 계산하세요
sum = scores["영어"] + scores["국어"]
avg = sum / 2
print("평균 점수:", avg)   # 94.0

# 다음과 같이 카페 메뉴 딕셔너리를 만드세요
