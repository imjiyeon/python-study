# 쇼핑몰 장바구니를 딕셔너리로 만드세요
# 상품명: "사과", "바나나", "딸기"
# 수량: 각각 3, 5, 2
cart = {"사과": 3, "바나나": 5, "딸기": 2}
print(cart)
# 상품의 수량을 10으로 변경하세요
cart["사과"] = 10
print(cart)
# 바나나를 삭제하세요
del cart["바나나"]
print(cart)

# 다음 시험점수를 딕셔너리로 만드세요
