# 카페 메뉴 가격을 딕셔너리로 만드세요
# 메뉴: "아메리카노", "라떼", "케이크"
# 가격: 각각 2000, 3000, 4500
menu = {"아메리카노": 2000, "라떼": 3000, "케이크": 4500}
# 아메리카노 가격을 2500으로 변경하세요
menu["아메리카노"] = 2500
print(menu)
# 케이크를 삭제하세요
del menu["케이크"]
print(menu)