# 두 개의 숫자를 입력받아, 첫 번째 수를 두 번째 수로 나눈 몫을 출력하는 함수를 작성하고 호출하세요
# 단, 두 번째 수가 0이면 "나누는 수는 0이 될 수 없습니다"를 출력하고 함수를 종료하세요
# 예)
# 입력: 15, 5 → 출력: 3
# 입력: 10, 0 → 출력: 나누는 수는 0이 될 수 없습니다

def divide(n1, n2):
    if n2 == 0:
        print("나누는 수는 0이 될 수 없습니다")
        return
    
    result = n1 // n2
    print(f"{n1} / {n2} = {result}")

divide(15, 5)
divide(10, 0)
