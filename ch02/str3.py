#문자열 연결 연산자 +
print("안녕"+"하세요")
print("혼자" + " " + "공부하는" + " " + "첫" + " " + "프로그래밍!")

#문자열 반복 연산자 *
print("*" * 2)

#공백문자 2개 만들고 *랑 연결
print(" " * 2 + "*")
print(" " * 3 + 1 * "*")
print(" " * 4 + 1 * "*")
print(" " * 5 + 2 * "*")

#문자열 길이를 구하는 함수: len
print(len("AAA"))
print(len("Hello, World"))
print(len("11111"))
print(len("안녕하세요"))
