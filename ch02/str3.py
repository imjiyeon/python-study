#문자열 연결 연산자 +
print("대한민국을" + " " + "빛낼" + " " + "프로그래밍" + " " + "입문서!")
print("혼자" + " " + "공부하는" + " " + "첫" + " " + "프로그래밍!")
print("혼공" + "족")
print("혼공" + "프로")
print("혼공" + "파")

#문자열 반복 연산자 *
print("*" * 2)

#공백문자 2개 만들고 *랑 연결
print(" " * 2 + "*")
print(" " * 3 + 1 * "*")
print(" " * 4 + 1 * "*")
print(" " * 5 + 2 * "*")

#문자열 길이를 구하는 함수: len
print(len("AAA"))
print(len("Hello, World"))
print(len("77791022723607"))
print(len("파이썬, 안녕?"))
print(len("\"아!\""))
