# 문자 데이터를 위한 연산자

#문자열 연결 연산자 +
print("안녕"+"하세요")
print("혼자" + " " + "공부하는" + " " + "첫" + " " + "프로그래밍!")

# 문자열 연결 연산자+ 와 더하기 연산자+ 의 차이
print("2" + "3")   # "23" (문자열 이어붙이기)
print(2 + 3)       # 5    (숫자 덧셈)

#문자열 반복 연산자 *
print("*" * 2)

# 연결연산자와 반복연사자를 사용해 동물소리 만들기
print("멍" * 3 + "!" )   # 멍멍멍!

