# 형변환
# 형변환은 자료형(int, float, str, bool)을 서로 바꾸는 것이다.

# float -> int 
# 소수점 이하 버림
print(int(3.23))

# str -> int
# 숫자 형태의 문자열만 가능
print(int("323"))

# int -> float
print(float(323))

# str -> float
# 실수 형태의 문자열만 가능
print(float("3.23"))

# int -> bool
# 0은 False, 나머지는 True
print(bool(0))
print(bool(1))

# float -> bool
# 0.0은 False, 나머지는 True
print(bool(3.23))

# int -> str
print(str(123))     # "123"

# float -> str
print(str(3.14))    # "3.14"

