#문자데이터 만들기

#문자의 양끝을 큰따옴표 또는 작은따옴표로 감싸기
#print는 문자열을 출력하는 함수
#함수란? 파이썬에서 제공하는 기능

print("A")
print("Hello, World!")
print("혼자 공부하는 첫 프로그래밍!")

print('안녕하세요')
print('323')

#이스케이프 문자
#따음표는 문자데이터를 표현할 때 사용하기 때문에
#따음표 자체를 표시하고 싶으면 이스케이프 문자를 사용해야한다
print("\"하나\"둘")

#이스케이프 문자: 역슬래시 + 문자 조합
#이스케이프문자는 문자 그자체나 특수문자를 표현할 때 사용한다

#\n -> 줄바꿈
print("Hello\nWorld!")
