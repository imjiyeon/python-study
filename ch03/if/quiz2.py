# 성별을 저장할 변수를 선언하고 성별을 'F' 또는 'M'으로 대입하세요
# 변수의 값이 F이면 "여성입니다", M이면 "남성입니다"를 출력하세요
gender = 'F'
if gender == 'F':
    print("여성입니다")
else:
    print("남성입니다")

# 나이를 저장할 변수를 선언하고 숫자를 대입하세요
# 나이가 65세 이상이면 "무료 승차", 그렇지 않으면 "유료 승차"를 출력하세요
age = 70
if age >= 65:
    print("무료 승차")
else:
    print("유료 승차")
