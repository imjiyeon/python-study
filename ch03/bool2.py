#비교연산자: == > <
#비교연산자로 조건식 만들기
print(1 == 1)
print(2 != 1)
print("abc" == " abc ")
print("Abc" != "abc")
print(2 > 1)
print(2 < 1)
print(3 >= 3) 
print(4 <= 2)  

#논리연산자: and or not
#논리연산자로 조건식 만들기
print(True and True) #양쪽 모두 참
print(True and False)  

print(True or False) #한쪽만 참이어도 참
print(False or False)

print(not True) # 반대로 바뀜
print(not False) 