# len : 길이 구하기
print(len("python"))
print(len([1, 2, 3]))
print(len((1, 'a')))

# list : 자료구조 → 리스트로 변환
print(list("python"))
print(list((1, 2, 3)))

# max : 최댓값
print(max([1, 2, 3]))
print(max("python"))

# min : 최솟값
print(min([1, 2, 3]))
print(min("python"))

# range : 범위 생성
print(list(range(5)))
print(list(range(5, 10)))
print(list(range(1, 10, 2)))
print(list(range(0, -10, -1)))

# round : 반올림
print(round(4.6))
print(round(4.2))
print(round(5.678, 2))
