# 리스트의 최대값과 최소값을 구하세요
# 리스트: [3, 1, 7, 4]
nums = [3, 1, 7, 4]
print(max(nums))  # 7
print(min(nums))  # 1

# 리스트를 정렬하세요
# 리스트: [10, 2, 33, 25]
nums = [10, 2, 33, 25]
print(sorted(nums))

# 리스트의 합계를 구하세요
# 리스트: [5, 10, 15]
nums = [5, 10, 15]
print(sum(nums))

# 숫자 -7과 -3.14의 절댓값을 구하세요
print(abs(-7))     # 7
print(abs(-3.14))  # 3.14

# "hello", [1, 2, 3], 3.14의 자료형을 각각 확인하세요
print(type("hello"))   # <class 'str'>
print(type([1, 2, 3])) # <class 'list'>
print(type(3.14))      # <class 'float'>
