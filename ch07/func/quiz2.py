# 리스트의 최대값과 최소값을 구하세요
# 리스트: [8, 12, -5, 0]
nums = [8, 12, -5, 0]
print(max(nums))  # 12
print(min(nums))  # -5

# 리스트를 정렬하세요
# 리스트: [42, 7, 19, 3]
nums = [42, 7, 19, 3]
print(sorted(nums))  # [3, 7, 19, 42]

# 리스트의 합계를 구하세요
# 리스트: [100, 200, 300]
nums = [100, 200, 300]
print(sum(nums))  # 600

# 숫자 -15와 -2.71의 절댓값을 구하세요
print(abs(-15))     # 15
print(abs(-2.71))   # 2.71

# "world", (10, 20, 30), 99의 자료형을 각각 확인하세요
print(type("world"))      # <class 'str'>
print(type((10, 20, 30))) # <class 'tuple'>
print(type(99))           # <class 'int'>
